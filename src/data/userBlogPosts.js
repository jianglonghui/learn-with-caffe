export const generateFullBlogArticle = async (userId, postId) => {
  const posts = generateUserBlogPosts(userId);
  const post = posts.find(p => p.id === postId);
  
  if (!post) return null;

  const contentGenerators = {
    'xiaoyu': generateXiaoyuContent,
    'laochen': generateLaochenContent,
    'linainai': generateLinainaiContent
  };

  const generator = contentGenerators[userId] || generateDefaultContent;
  const content = generator(post);

  return {
    ...post,
    content,
    author: getUserInfo(userId).name,
    date: new Date().toLocaleDateString('zh-CN')
  };
};

